/**
 *
 * @author hangnt
 */
public class SinhVien {

    private String studentId;
    private String name;
    private int age;

    public SinhVien() {
    }

    public SinhVien(String studentId, String name, int age) {
        this.studentId = studentId;
        this.name = name;
        this.age = age;
    }

    public String getStudentId() {
        return studentId;
    }

    public void setStudentId(String studentId) {
        this.studentId = studentId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "SinhVien{" + "studentId=" + studentId + ", name=" + name + ", age=" + age + '}';
    }

}
